//! Refresh the FASTA → SBOL 3 conformance snapshots.
//!
//! Writes `tests/fixtures/fasta/expected/{name}.nt` for every
//! committed `.fasta` / `.fa` / `.fna` / `.faa` file under
//! `tests/fixtures/fasta/`. Diffing against these snapshots is the
//! critical conformance gate for the `sbol-fasta` importer. The
//! whole regen runs in pure Rust — no Docker, no Python, no Node.
//!
//! Usage:
//!
//!     cargo run -p sbol-fasta --bin regenerate-fasta-import-snapshots
//!
//! Exits non-zero if the importer fails on any fixture.

use std::path::{Path, PathBuf};
use std::process::ExitCode;

use sbol::{Resource, Term, Triple};
use sbol_fasta::FastaImporter;

fn workspace_root() -> PathBuf {
    let mut path = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    path.pop();
    path.pop();
    path
}

fn main() -> ExitCode {
    let root = workspace_root();
    let fixtures_dir = root.join("tests/fixtures/fasta");
    let expected_dir = fixtures_dir.join("expected");

    let mut paths: Vec<PathBuf> = match std::fs::read_dir(&fixtures_dir) {
        Ok(entries) => entries
            .filter_map(|e| e.ok().map(|e| e.path()))
            .filter(|p| {
                matches!(
                    p.extension().and_then(|s| s.to_str()),
                    Some("fasta") | Some("fa") | Some("fna") | Some("faa")
                )
            })
            .collect(),
        Err(err) => {
            eprintln!(
                "failed to read fixtures dir {}: {err}",
                fixtures_dir.display()
            );
            return ExitCode::from(2);
        }
    };
    paths.sort();

    if let Err(err) = std::fs::create_dir_all(&expected_dir) {
        eprintln!(
            "failed to create expected dir {}: {err}",
            expected_dir.display()
        );
        return ExitCode::from(2);
    }

    let mut failures = 0usize;
    let mut regenerated = Vec::new();

    for path in &paths {
        let name = path.file_stem().unwrap().to_string_lossy().to_string();
        let namespace = format!("https://sbol-rs.example.org/fasta/{name}");
        let importer = match FastaImporter::new(&namespace) {
            Ok(importer) => importer,
            Err(err) => {
                eprintln!("[FAIL] {name}: namespace: {err}");
                failures += 1;
                continue;
            }
        };
        let (document, _report) = match importer.read_path(path) {
            Ok(pair) => pair,
            Err(err) => {
                eprintln!("[FAIL] {name}: import: {err}");
                failures += 1;
                continue;
            }
        };
        let triples = document.rdf_graph().normalized_triples();
        let dest = expected_dir.join(format!("{name}.nt"));
        if let Err(err) = write_snapshot(&name, &triples, &dest) {
            eprintln!("[FAIL] {name}: write {}: {err}", dest.display());
            failures += 1;
            continue;
        }
        regenerated.push((name, triples.len()));
    }

    for (name, count) in &regenerated {
        println!("regenerated {name} ({count} triples)");
    }
    println!("{} fixture(s) regenerated", regenerated.len());
    if failures > 0 {
        eprintln!("{failures} regeneration failure(s)");
        return ExitCode::from(1);
    }
    ExitCode::SUCCESS
}

fn write_snapshot(name: &str, triples: &[Triple], destination: &Path) -> std::io::Result<()> {
    let mut lines: Vec<String> = triples.iter().map(format_triple).collect();
    lines.sort();
    lines.dedup();
    let mut body = lines.join("\n");
    if !body.is_empty() {
        body.push('\n');
    }
    let header = format!(
        "# Regenerated by `cargo run -p sbol-fasta \\\n\
         #   --bin regenerate-fasta-import-snapshots`.\n\
         # Source fixture: tests/fixtures/fasta/{name}.{{fasta|fa|fna|faa}}\n"
    );
    if let Some(parent) = destination.parent() {
        std::fs::create_dir_all(parent)?;
    }
    std::fs::write(destination, format!("{header}{body}"))?;
    Ok(())
}

fn format_triple(triple: &Triple) -> String {
    const XSD_STRING: &str = "http://www.w3.org/2001/XMLSchema#string";
    let subject = match &triple.subject {
        Resource::Iri(iri) => format!("<{}>", iri.as_str()),
        Resource::BlankNode(b) => format!("_:{}", b.as_str()),
        _ => format!("{:?}", triple.subject),
    };
    let predicate = format!("<{}>", triple.predicate.as_str());
    let object = match &triple.object {
        Term::Resource(Resource::Iri(iri)) => format!("<{}>", iri.as_str()),
        Term::Resource(Resource::BlankNode(b)) => format!("_:{}", b.as_str()),
        Term::Literal(literal) => {
            let escaped = escape_nt_string(literal.value());
            if let Some(lang) = literal.language() {
                format!("\"{escaped}\"@{lang}")
            } else if literal.datatype().as_str() == XSD_STRING {
                format!("\"{escaped}\"")
            } else {
                format!("\"{escaped}\"^^<{}>", literal.datatype().as_str())
            }
        }
        _ => format!("{:?}", triple.object),
    };
    format!("{subject} {predicate} {object} .")
}

fn escape_nt_string(value: &str) -> String {
    let mut out = String::with_capacity(value.len());
    for c in value.chars() {
        match c {
            '\\' => out.push_str("\\\\"),
            '"' => out.push_str("\\\""),
            '\n' => out.push_str("\\n"),
            '\r' => out.push_str("\\r"),
            '\t' => out.push_str("\\t"),
            _ => out.push(c),
        }
    }
    out
}
