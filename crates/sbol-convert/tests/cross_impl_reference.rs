//! Cross-implementation differential test against the SynBioDex/SBOL-Converter
//! Java reference. For each committed reference conversion under
//! `tests/fixtures/cross-impl-sbolconverter/expected/` this runs the full
//! matrix and compares normalized RDF triple sets:
//!
//! 1. Same-direction agreement (2->3): reference vs sbol-rs, same SBOL 2 input.
//! 2. Same-direction agreement (3->2): reference vs sbol-rs, same SBOL 3 input.
//! 3. Cross-tool round-trip: reference 2->3, then sbol-rs 3->2, vs original.
//! 4. Cross-tool round-trip: reference 3->2, then sbol-rs 2->3, vs original.
//!
//! The reference outputs are generated by
//! `regenerate-cross-impl-reference` (Docker, contributor-only); this test
//! needs no Docker/JVM.
//!
//! Comparison is on logical equivalence. [`logical`] content-addresses the
//! auto-generated `Constraint<N>` IRIs — the reference assigns their ordinal in
//! its Java `HashSet` iteration order, so the label a given constraint receives
//! carries no meaning — letting a label permutation compare equal while any
//! real structural difference still fails. [`logical_roundtrip`] additionally
//! strips conversion provenance and collapses version-equivalent references, so
//! a reference to an object's persistent identity matches a reference to its
//! single concrete version.
//!
//! GATED (must be logically equivalent):
//! - Same-direction agreement: reference and sbol-rs convert identical input
//!   the same way, in each direction.
//! - `ref-up->rs-down`: SBOL 2 -> reference SBOL 3 -> sbol-rs SBOL 2 == original.
//!
//! REPORTED, not gated:
//! - `ref-down->rs-up`: SBOL 3 -> reference SBOL 2 -> sbol-rs SBOL 3 vs original
//!   SBOL 3. SBOL 2 cannot represent a feature's orientation or a
//!   FunctionalComponent's locations, so the SBOL 3 -> SBOL 2 step discards them
//!   (both tools agree on that SBOL 2). Re-upgrading cannot restore what the
//!   SBOL 2 model does not hold; the residual is that model-expressiveness gap.

