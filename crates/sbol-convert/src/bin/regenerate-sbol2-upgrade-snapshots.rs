//! Refresh the SBOL 2 → SBOL 3 conformance snapshots.
//!
//! For every committed fixture under `tests/fixtures/sbol2/real/`,
//! re-runs `sbol_convert::upgrade_from_sbol2` and writes the canonicalized
//! N-Triples output to `tests/fixtures/sbol2/real/expected/{name}.nt`.
//! The integration test in `crates/sbol-convert/tests/upgrade_conformance.rs`
//! diffs against these snapshots on every test run.
//!
//! Usage:
//!
//!     cargo run -p sbol-convert --bin regenerate-sbol2-upgrade-snapshots
//!
//! Pure Rust. No external tools.

use std::path::{Path, PathBuf};
use std::process::ExitCode;

use sbol3::{RdfFormat, Triple};

fn workspace_root() -> PathBuf {
    let mut path = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    path.pop();
    path.pop();
    path
}

/// Walks `fixtures_dir` recursively, returning every `*.xml` file that is
/// not inside the snapshot output directory.
fn collect_fixture_paths(
    fixtures_dir: &Path,
    expected_dir: &Path,
) -> std::io::Result<Vec<PathBuf>> {
    fn walk(dir: &Path, expected_dir: &Path, out: &mut Vec<PathBuf>) -> std::io::Result<()> {
        for entry in std::fs::read_dir(dir)? {
            let entry = entry?;
            let path = entry.path();
            if path == *expected_dir {
                continue;
            }
            let file_type = entry.file_type()?;
            if file_type.is_dir() {
                walk(&path, expected_dir, out)?;
            } else if file_type.is_file()
                && path.extension().and_then(|s| s.to_str()) == Some("xml")
            {
                out.push(path);
            }
        }
        Ok(())
    }
    let mut out = Vec::new();
    walk(fixtures_dir, expected_dir, &mut out)?;
    Ok(out)
}

/// Maps an absolute fixture path to a slash-separated fixture name relative
/// to `fixtures_dir`. The `.xml` extension is dropped. For example,
/// `tests/fixtures/sbol2/real/synbiohub/BBa_E0040.xml` becomes
/// `synbiohub/BBa_E0040`. This preserves provenance grouping (SBOLTestSuite
/// vs. SynBioHub vs. from_genbank) in the snapshot filesystem layout.
fn fixture_name(path: &Path, fixtures_dir: &Path) -> String {
    let relative = path
        .strip_prefix(fixtures_dir)
        .unwrap_or(path)
        .with_extension("");
    relative
        .components()
        .filter_map(|c| match c {
            std::path::Component::Normal(s) => s.to_str(),
            _ => None,
        })
        .collect::<Vec<&str>>()
        .join("/")
}

fn write_snapshot(name: &str, triples: &[Triple], destination: &Path) -> std::io::Result<()> {
    let mut lines: Vec<String> = triples.iter().map(format_triple).collect();
    lines.sort();
    lines.dedup();
    let mut body = lines.join("\n");
    if !body.is_empty() {
        body.push('\n');
    }
    let header = format!(
        "# Regenerated by `cargo run -p sbol-convert --bin regenerate-sbol2-upgrade-snapshots`.\n\
         # Source fixture: tests/fixtures/sbol2/real/{name}.xml\n"
    );
    if let Some(parent) = destination.parent() {
        std::fs::create_dir_all(parent)?;
    }
    std::fs::write(destination, format!("{header}{body}"))?;
    Ok(())
}

fn format_triple(triple: &Triple) -> String {
    sbol_convert::canonical_nt_line(triple)
}

fn main() -> ExitCode {
    let root = workspace_root();
    let fixtures_dir = root.join("tests/fixtures/sbol2/real");
    let expected_dir = root.join("tests/fixtures/sbol2/real/expected");

    let mut paths: Vec<PathBuf> = match collect_fixture_paths(&fixtures_dir, &expected_dir) {
        Ok(paths) => paths,
        Err(err) => {
            eprintln!(
                "failed to read fixtures dir {}: {err}",
                fixtures_dir.display()
            );
            return ExitCode::from(2);
        }
    };
    paths.sort();

    let mut failures = 0usize;
    let mut regenerated = Vec::new();

    for path in &paths {
        let name = fixture_name(path, &fixtures_dir);
        let bytes = match std::fs::read_to_string(path) {
            Ok(text) => text,
            Err(err) => {
                eprintln!("[FAIL] {name}: read {}: {err}", path.display());
                failures += 1;
                continue;
            }
        };
        let (document, _report) = match sbol_convert::upgrade_from_sbol2(&bytes, RdfFormat::RdfXml)
        {
            Ok(pair) => pair,
            Err(err) => {
                eprintln!("[FAIL] {name}: upgrade: {err}");
                failures += 1;
                continue;
            }
        };
        let triples = document.rdf_graph().normalized_triples();
        let dest = expected_dir.join(format!("{name}.nt"));
        if let Err(err) = write_snapshot(&name, &triples, &dest) {
            eprintln!("[FAIL] {name}: write {}: {err}", dest.display());
            failures += 1;
            continue;
        }
        regenerated.push((name.clone(), triples.len()));
        println!("regenerated {name} ({} triples)", triples.len());
    }

    println!(
        "{} snapshots regenerated; {failures} failure(s)",
        regenerated.len()
    );
    if failures == 0 {
        ExitCode::SUCCESS
    } else {
        ExitCode::from(1)
    }
}
