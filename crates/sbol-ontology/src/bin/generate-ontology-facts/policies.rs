//! Static term, branch, and compatibility policies that enrich the raw
//! ontology cache into the bundled SBOL fact snapshot. Pure data tables
//! consumed by the generator in the sibling binary entry point.

#[derive(Clone, Debug)]
pub(crate) struct TermPolicy {
    pub(crate) id: &'static str,
    pub(crate) iri: &'static str,
    pub(crate) label: &'static str,
    pub(crate) aliases: &'static [&'static str],
    pub(crate) parents: &'static [&'static str],
    pub(crate) ontology: &'static str,
    pub(crate) role: &'static str,
    pub(crate) component_family: &'static str,
    pub(crate) sequence_family: &'static str,
    pub(crate) table_1: bool,
    pub(crate) table_2: bool,
}

#[derive(Clone, Copy, Debug)]
pub(crate) struct BranchPolicy {
    pub(crate) root_id: &'static str,
    pub(crate) role: &'static str,
    pub(crate) component_family: &'static str,
    pub(crate) sequence_family: &'static str,
}

